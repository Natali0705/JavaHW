package class8;

public class E10NestedLoop {
    //5 *  5 lines
    public static void main(String[] args) {

        /*
        # # # # #
        # # # # #
        # # # # #
        # # # # #
        # # # # #
         */

        for (int j = 0; j < 5; j++) {
            for (int i = 0; i < 5; i++) {
                System.out.print("# ");
            }
            System.out.println();
        }

    }
}
